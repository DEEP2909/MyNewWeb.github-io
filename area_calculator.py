def calculate_area(points):
    """
    Calculate the area of a polygon on Earth's surface.
    points: list of (latitude, longitude) tuples
    returns: area in square meters
    """
    # Convert points to (longitude, latitude) format for shapely
    points_lonlat = [(lon, lat) for lat, lon in points]
    
    # Create a polygon from the points
    polygon = Polygon(points_lonlat)
    
    # Get the coordinates of the polygon
    coords = np.array(polygon.exterior.coords)
    
    # Calculate the area using pyproj's Geod
    geod = Geod(ellps='WGS84')
    area = abs(geod.geometry_area_perimeter(polygon)[0])
    
    return area

